const kopfdatenSchema = z
	.object({
		anbringen: z.literal("BET"),
		jahr: gYear,
		fastnr,
		name: an37,
		adrBetr: an37,
		ortBetr: an37,
		plzBetr: plz,
		plzOrtBetr: an37.optional(),
		landBetr: landBetrSchema,
		datum,
		uhrzeit,
	})
	.strict();

const beteiligterSchema = z
	.object({
		fastnrb: fastnr,
		pro: prozent,
		jahrpro: prozent,
		zrvon: tagmonat,
		zrbis: z.string().min(1).optional(),
		folge: folgeSchema,
		nameb: an20,
	})
	.strict();

export const betBody = z
	.object({
		kopf: kopfdatenSchema,
		beteiligte: z.array(beteiligterSchema).min(1).max(200),
	})
	.strict();

export type BetBodyParsed = z.infer<typeof betBody>;
