import { z } from "zod";

const fastnr = z
	.string()
	.regex(/^[0-9]{9}$/, "Expected 9 digits")
	.refine((s) => {
		const n = Number(s);
		return n >= 30_000_010 && n <= 989_999_999;
	}, "Out of valid FASTNR range");

const datum = z
	.string()
	.regex(/^[0-9]{4}-(?:0[1-9]|1[0-2])-(?:0[1-9]|[12][0-9]|3[01])$/, "Expected YYYY-MM-DD");
const uhrzeit = z
	.string()
	.regex(/^(?:[01][0-9]|2[0-3]):[0-5][0-9]:[0-5][0-9]$/, "Expected HH:MM:SS");
const gYear = z.string().regex(/^[0-9]{4}$/, "Expected YYYY");

/** DD-MM (BMF tagmonattype). */
const tagmonat = z.string().regex(/^[0-3][0-9]-[0-1][0-9]$/, "Expected DD-MM");

const prozent = z.number().min(0.00000001).max(100);

/** alphanumerisch37 — 1..37 chars. */
const an37 = z.string().min(1).max(37);
/** alphanumerisch20 — 1..20 chars. */
const an20 = z.string().min(1).max(20);
/** plz — 1..10 chars. */
const plz = z.string().min(1).max(10);

