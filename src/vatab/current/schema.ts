/** IBAN: EU prefix (29 supported) + 2 check digits + 12..30 alphanumerics. */
const ibanSchema = z
	.string()
	.regex(
		/^(AT|BE|BG|CY|CZ|DE|DK|EE|GR|ES|FI|FR|GB|HR|HU|IE|IT|LT|LU|LV|MT|NL|PL|PT|RO|SE|SI|SK|MC)[0-9]{2}[0-9A-Z]{10,30}$/,
		"Expected MS-IBAN format",
	);

const bicSchema = z.string().regex(/^[A-Z]{6}[A-Z0-9]{2}([A-Z0-9]{3})?$/, "Expected ISO 9362 BIC");

const kontoinhaberSchema = z
	.string()
	.min(1)
	.max(70)
	.regex(/^[A-Za-z0-9/\-?:().,+ ]*$/, "Expected restricted alphabet");

/** ANTRAGNR for VATAB — digits only after AT prefix (16 chars total). */
const antragnrSchema = z.string().regex(/^AT[0-9]{14}$/, "Expected AT + 14 digits");

/** zahl_16 — decimal -9999999999999.99..9999999999999.99. */
const zahl16 = z.number().min(-9_999_999_999_999.99).max(9_999_999_999_999.99);

