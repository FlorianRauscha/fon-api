const NACE_SET = new Set<string>(NACE_VALUES);

const naceSchema = z
	.string()
	.regex(/^[0-9]{4}$/, "Expected 4 digits")
	.refine((s) => NACE_SET.has(s), "Not a recognized NACE code");

const pdfAnhangSchema = z
	.object({
		base64: z.string().min(1),
	})
	.strict();

const anhangSchema = z
	.object({
		pdf: pdfAnhangSchema.optional(),
	})
	.strict();

const abschlussSchema = z
	.object({
		gesamtKauf: z.number().int().min(0).max(999_999).optional(),
		gesamtBmgKauf: zahl16.optional(),
		gesamtImport: z.number().int().min(0).max(999_999).optional(),
		gesamtBmgImport: zahl16.optional(),
		anhang: anhangSchema.optional(),
	})
	.strict();

const allgemeineDatenSchema = z
	.object({
		anbringen: z.literal("VATAB"),
		antragnr: antragnrSchema.optional(),
		zrvon: yyyymm,
		zrbis: yyyymm,
		fastnr,
		kundeninfo: z.string().max(50).optional(),
		euLand: euLandSchema,
		emailUnternehmer: emailSchema,
		emailVertreter: emailSchema.optional(),
		nace: z.array(naceSchema).min(1).max(5),
		kontoinhaber: kontoinhaberSchema,
		inhabertyp: inhabertypSchema,
		iban: ibanSchema,
		bic: bicSchema,
		waehrBank: waehrungSchema,
		frage1a: wahlSchema,
		frage1b: wahlSchema,
		frage1c: wahlSchema,
		frage2a: wahlSchema.optional(),
		frage2b: wahlSchema.optional(),
	})
	.strict();

const infoDatenSchema = z.object({
	artIdentifikationsbegriff: z.literal("FASTNR"),
	identifikationsbegriff: fastnr,
	paketNr: z.number().int().min(1).max(999_999_999),
	datumErstellung: datum,
	uhrzeitErstellung: uhrzeit,
	anzahlErklaerungen: z.number().int().min(1).max(300),
});

const erklaerungSchema = z
	.object({
		art: z.literal("VATAB"),
		satznr: z.number().int().min(1).max(999_999_999),
		allgemein: allgemeineDatenSchema,
		abschluss: abschlussSchema,
	})
	.strict();

export const vatabBody = z
	.object({
		info: infoDatenSchema,
		erklaerungen: z.array(erklaerungSchema).min(1).max(300),
	})
	.strict()
	.refine(
		(b) => b.info.anzahlErklaerungen === b.erklaerungen.length,
		"info.anzahlErklaerungen must equal erklaerungen.length",
	);

export type VatabBodyParsed = z.infer<typeof vatabBody>;
